public class Literals {
    public static void main(String[] args) {
        //Literals are the values that are assigned to the variable
        //For e.g. num1 = 0b101 here 0b101 is literal

        int num1 = 0b101;
        int num2 = 123_345_67;
        double num3 = 12e10;
        char num4 = 'a';
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        num4++;
        System.out.println(num4);

    }
}
