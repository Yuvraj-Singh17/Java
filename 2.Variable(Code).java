// Variables are the containers used to store data
public class variable_s {
    // Java is strongly typed Language means you have to the datatype of the variable
    public static void main(String[] args) {
        int a = 5; //Here DataType of the Variable a is int(integer)
        int b = 95;//Here DataType of the variable b is int(integer) 
        String str = "Virat Kohli"; //Here DataType of the Variable str is String
        float u = 4.9f;//Here DataType of the variable u is float(Number with Decimal)
        double w = 5667.4;//Here DataType of the variable w is double(Larger Size than Float)
        int sum = a + b ;
        System.out.println(sum);// for this we can do it in another way that is in next line 
        System.out.println(a+b);
        System.out.println(str);
        System.out.println(u);
        System.out.println(w);
        //After Assigning Different type of variable I am trying to print them all
    }
}
